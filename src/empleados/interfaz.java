                        op3.setHorasTrabajadas(horasTrabajadas);
                        op3.setValor(valorHora);
                        op3.setExtra(Integer.parseInt(txtDinamico.getText()));

                        String mostrarInfoOp3 = op3.mostrar();

                        viewOpe.setVisible(true);

                        viewOpe.setText(mostrarInfoOp3);
