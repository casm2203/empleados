                        op1.setNombre(nombre);
                        op1.setApellido(apellido);
                        op1.setDirrecion(dirrecion);
                        op1.setCelular(celular);
                        op1.setCorreo(correo);
                        op1.setTipo(cbxTipo.getSelectedItem().toString());
